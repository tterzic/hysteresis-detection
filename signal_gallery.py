# Main
# ---------------------------------------------------------------------------

def plot_signal_gallery(
    K             : int   = 1000,
    N             : int   = 10,
    delta_t       : float = 1.0,
    sigma_HR_rise : float = 0.5,
    sigma_HR_decay: float = 2.0,
    sigma_F_rise  : float = 2.0,
    sigma_F_decay : float = 0.5,
    sigma_noise   : float = 0.05,
    n_sigma_rise  : float = 2.0,
    n_sigma_decay : float = 2.0,
    seed          : int   = 42,
    outdir        : str   = "results",
    idx_a         : int   = 777,
    idx_b         : int   = 225,
    idx_c         : int   = 839,
    idx_d         : int   = 543,
):
    """
    Generate the signal gallery figure and save trajectories as CSV files.

    With default parameters this reproduces the paper figure exactly.
    The trajectory labels ('Clean single loop', 'Incomplete path', etc.) describe the specific trajectories selected by the default pool indices (777, 225, 839, 543) with seed=42 and K=1000. If any of these are changed, the selected trajectories will be different and the labels will no longer be meaningful.

    Run analyse_hysteresis.py on the saved CSV files for the analysis.
    """
    rng = np.random.default_rng(seed)
    Path(outdir).mkdir(parents=True, exist_ok=True)

    # Warn if non-default parameters are used, since the trajectory labels
    # only apply to the default configuration.
    non_default = (seed != 42 or K != 1000 or
                   idx_a != 777 or idx_b != 225 or idx_c != 839 or idx_d != 543)
    if non_default:
        print("WARNING: non-default seed, K, or pool indices detected.")
        print("         The trajectory labels (Clean single loop; Incomplete path; etc.)")
        print("         describe the default paper trajectories and may not apply here.")

    kwargs = dict(
        N=N, delta_t=delta_t,
        sigma_HR_rise=sigma_HR_rise, sigma_HR_decay=sigma_HR_decay,
        sigma_F_rise=sigma_F_rise,   sigma_F_decay=sigma_F_decay,
        sigma_noise=sigma_noise,
        n_sigma_rise=n_sigma_rise,   n_sigma_decay=n_sigma_decay,
    )

    print(f"Generating pool of {K} trajectories (seed={seed})...")
    pool = generate_pool(K, kwargs, rng)
    print(f"Done. Pool size: {len(pool)}")

    selections = [
        (idx_a, "(a) Clean single loop", "a"),
        (idx_b, "(b) Incomplete path",   "b"),
        (idx_c, "(c) Figure-eight",      "c"),
        (idx_d, "(d) Overlapping paths", "d"),
    ]

    # --- Save trajectories as CSV files ---
    # These are the input files for analyse_hysteresis.py.
    print("\nSaving trajectories:")
    for idx, label, letter in selections:
        x, y, sx, sy = pool[idx - 1]
        csv_path = Path(outdir) / f"signal_{letter}.csv"
        save_csv(x, y, sx, sy, label, idx, seed, csv_path)
        print(f"  {csv_path}")

    # --- Figure ---
    # Axis labels match the CSV header row written by save_csv
    x_label = "F (A.U.)"
    y_label = "HR"

    fig, axes = plt.subplots(2, 2, figsize=(3.46, 3.40), constrained_layout=True)

    for ax, (idx, title, _) in zip(axes.flatten(), selections):
        x, y, sx, sy = pool[idx - 1]
        draw_panel(ax, x, y, sx, sy, title, x_label, y_label)

    cmap = plt.get_cmap("viridis")
    sm   = plt.cm.ScalarMappable(cmap=cmap, norm=mcolors.Normalize(vmin=1, vmax=N))
    sm.set_array([])
    cbar = fig.colorbar(sm, ax=axes, orientation="vertical",
                        fraction=0.03, pad=0.02, aspect=30)
    cbar.set_label("Time step", fontsize=7)
    cbar.ax.tick_params(labelsize=6)

    fig_path = str(Path(outdir) / "signal_gallery.pdf")
    fig.savefig(fig_path, bbox_inches="tight", dpi=300)
    print(f"\nSaved figure: {fig_path}")
    print(f"\nNext step: run analyse_hysteresis.py on each CSV file, e.g.:")
    for _, _, letter in selections:
        print(f"  python analyse_hysteresis.py "
              f"{Path(outdir) / f'signal_{letter}.csv'} --K_null 10000")
    plt.close(fig)


