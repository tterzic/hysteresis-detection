"""
signal_gallery.py
=================
Generate Fig. X (signal gallery) of Terzic (2026): four representative trajectory types in the hardness ratio--flux (HR, F) plane.

This script does two things only:
  1. Generates and saves four example trajectories as CSV data files.
  2. Produces the signal gallery figure.

It does NOT perform any statistical analysis. The analysis (A_norm, closure and cancellation diagnostics, MC uncertainty propagation, p-values) should be performed separately by running analyse_hysteresis.py on the saved CSV files, exactly as one would for real observational data.

Output files
------------
All written to --outdir (default: results/):

  signal_gallery.pdf
      The 2x2 figure with the four example trajectories.

  signal_a.csv, signal_b.csv, signal_c.csv, signal_d.csv
      One CSV file per trajectory, with the following format:

          # Trajectory: (a) Clean single loop
          # Generated by signal_gallery.py (seed=42; pool_index=777)
          # Columns: F (A.U.); HR; s_F; s_HR
          # F (A.U.) : flux (normalised to quiescent = 1)
          # HR       : hardness ratio (normalised to quiescent = 1)
          # s_F      : 1-sigma uncertainty on F (fractional: s_F  = sigma_noise * F)
          # s_HR     : 1-sigma uncertainty on HR (fractional: s_HR = sigma_noise * HR)
          F (A.U.),HR,s_F,s_HR
          1.456,1.234,0.073,0.062
          ...

      These files are the input for analyse_hysteresis.py (see below).

Input format for analyse_hysteresis.py
---------------------------------------
analyse_hysteresis.py expects a CSV file where the first non-comment line is the header row with four column names (used as axis labels). All subsequent lines are data rows read by column position. Rows beginning with '#' are treated as comments and ignored. Example usage:

    python analyse_hysteresis.py signal_a.csv --K_null 10000

This will compute A_norm; R_cancel; f_closure; d_closure; the MC uncertainty interval for A_norm; and all four p-values (p_perm; p_AR; p_Fourier; p_full).

Trajectory selection
--------------------
Trajectories are drawn from a pool of K randomly sampled asymmetric Gaussian flares (see Sect. 4 of the paper). The four panels are selected from this pool by index (1-indexed, reproducible via --seed):

  (a) #777  Clean single loop      -- large |A_norm|, large R_cancel
  (b) #225  Incomplete path        -- large f_closure, large d_closure
  (c) #839  Figure-eight           -- small |A_norm|, small R_cancel
  (d) #543  Overlapping paths      -- small |A_norm|, large R_cancel

The pool indices and seed are fixed to reproduce the paper figure.
To explore other trajectories, use --idx_a/b/c/d and --seed.
Note: the trajectory labels ('Clean single loop', etc.) only apply to the default configuration (seed=42, K=1000, default indices). Using different values will produce different trajectories with potentially misleading labels.

Flare model
-----------
HR(t) = 1 + A_HR * exp(-0.5 * ((t - t_HR) / sigma_HR_pm)^2)
F(t)  = 1 + A_F  * exp(-0.5 * ((t - t_F)  / sigma_F_pm)^2)

where sigma_HR_pm = sigma_HR_rise for t < t_HR, sigma_HR_decay otherwise, and analogously for F. The time delay is delta_t = t_F - t_HR.
Measurement uncertainties are fractional:
  s_f  = sigma_noise * F
  s_hr = sigma_noise * HR

Usage
-----
    python signal_gallery.py [options]

    Key options:
      --K       INT    Pool size (default 1000)
      --seed    INT    Random seed (default 42)
      --outdir  STR    Output directory (default results)
      --idx_a/b/c/d INT  Pool indices for each panel (default as above)

    To reproduce the paper figure exactly, use the defaults.

Dependencies
------------
    numpy, matplotlib
    generate_candidates.py  -- flare model and pool generation

Author: T. Terzić <tterzic@phy.uniri.hr>
License: MIT
"""

