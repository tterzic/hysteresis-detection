"""
null_gallery.py
===============
Generate Fig. X (null gallery) of Terzić (2026): one noise realisation of the case (a) signal trajectory alongside three null model realisations.

This script does two things only:
  1. Generates and saves four trajectories as CSV data files.
  2. Produces the null gallery figure.

It does NOT perform any statistical analysis. The analysis (A_norm, closure and cancellation diagnostics, MC uncertainty propagation, p-values) should be performed separately by running analyse_hysteresis.py on the saved CSV files.

Input
-----
This script reads signal_a.csv produced by signal_gallery.py. Run signal_gallery.py first. The input CSV must have the format:

    # comment lines starting with # are ignored
    F (A.U.),HR,s_F,s_HR
    1.456,1.234,0.073,0.062
    ...

where F and HR are the noise-free signal values and s_F; s_HR are their fractional 1-sigma uncertainties (s_F = sigma_noise * F; s_HR = sigma_noise * HR).

Output files
------------
All written to --outdir (default: results/):

  null_gallery.pdf
      The 2x2 figure with the four panels.

  null_a.csv  -- One noise realisation of the signal
      The noise-free signal perturbed by one draw from N(0; s_F) and N(0; s_HR). This represents one possible actual observation of the underlying signal trajectory. The uncertainties s_F; s_HR are preserved from the input file.

      Format identical to signal_a.csv. Suitable input for analyse_hysteresis.py.

  null_b.csv  -- Permutation null
      The same (F; HR) data points from the noise-free signal; with their time order randomly shuffled. No uncertainties are meaningful for null realisations (they are synthetic); so s_F = s_HR = 0 in this file. analyse_hysteresis.py will skip MC uncertainty propagation and d_closure when s_F = s_HR = 0.

  null_c.csv  -- AR(1) null
      A new pair of time series generated by fitting an AR(1) model to the noise-free signal coordinates and drawing one realisation. s_F = s_HR = 0.

  null_d.csv  -- Fourier phase-randomised null
      A new pair of time series with the same power spectrum as the noise-free signal but randomised phases s_F = s_HR = 0.

Input format for analyse_hysteresis.py
---------------------------------------
analyse_hysteresis.py expects a CSV where the first non-comment line is the header row with four column names (used as axis labels). Rows beginning with '#' are treated as comments and ignored.

For null_b/c/d.csv; s_F = s_HR = 0; so analyse_hysteresis.py will compute A_norm; R_cancel; f_closure but not d_closure or the MC uncertainty interval. Example usage:

    python analyse_hysteresis.py null_a.csv --K_null 10000
    python analyse_hysteresis.py null_b.csv --K_null 10000
    python analyse_hysteresis.py null_c.csv --K_null 10000
    python analyse_hysteresis.py null_d.csv --K_null 10000

Figure description
------------------
Panel (a): grey points with fractional error bars show the noise-free signal from signal_a.csv; coloured points show one noise realisation (saved as null_a.csv).
Panel (b): grey points with error bars show the original signal; coloured points show the permuted trajectory (null_b.csv).
Panels (c) and (d): AR(1) and Fourier null realisations respectively.
  Axes adapt to the range of each null.

The nulls in panels (b)--(d) are derived from the noise-free signal trajectory because the null models operate on the coordinate values, not the measurement noise.

Usage
-----
    python null_gallery.py [--signal_csv results/signal_a.csv]
                           [--outdir results] [--seed 43]

Dependencies
------------
    numpy, matplotlib
    signal_gallery.py    -- must be run first to produce signal_a.csv

Author: T. Terzić <tterzic@phy.uniri.hr>
License: MIT
"""

