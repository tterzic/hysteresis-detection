if __name__ == "__main__":
    parser = argparse.ArgumentParser(
        description=(
            "Generate null gallery figure and save trajectories as CSV. "
            "Requires signal_a.csv from signal_gallery.py. "
            "Run analyse_hysteresis.py on the CSV files for the analysis."
        ),
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument("--signal_csv", type=str, default="results/signal_a.csv",
                        help="Path to signal_a.csv from signal_gallery.py.")
    parser.add_argument("--seed",       type=int, default=43,
                        help="Random seed (should differ from signal_gallery seed).")
    parser.add_argument("--outdir",     type=str, default="results")
    args = parser.parse_args()

    plot_null_gallery(
        signal_csv = args.signal_csv,
        seed       = args.seed,
        outdir     = args.outdir,
    )
